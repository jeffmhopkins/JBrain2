"""Generate the llama-swap gateway config (llama-swap.yaml).

The single source of truth for the gateway's per-model `llama-server` command —
crucially the `-c` context window, which must equal what the router reports to the
PWA's context meter (jbrain.llm.router.context_window). Two callers share it so the
two never drift:

  - scripts/local-llm-setup.sh, at install time, via `python -m
    jbrain.llm.llama_swap_config <models_dir>` reading the MANIFEST env;
  - the settings API, at runtime, to re-stamp a model's `-c` after the operator
    edits its context window — written atomically into the mounted models dir,
    which the gateway (run with `--watch-config`) hot-reloads. A `-c` change takes
    effect on the model's next (re)load, so the API unloads a resident model after
    rewriting so its next request reloads at the new window.

Each manifest entry is a catalog dict (jbrain.llm.local_catalog.LocalModel
asdict): id, served_model, gguf_include, mmproj_include, context_window,
recommended. `windows` overrides a model's `-c` by catalog id.
"""

from __future__ import annotations

import glob
import json
import os
import sys
from collections.abc import Mapping, Sequence
from typing import cast

# Concrete, distinct upstream ports — llama-swap's ${PORT} macro isn't substituted
# by every build, and the resident group runs models concurrently so they can't
# share a port. 127.0.0.1: llama-swap and llama-server share the container.
UPSTREAM_PORT_BASE = 9100

# The matrix solver's relative cost of evicting a hot-set member: it sums the costs of
# the running models a candidate combination would have to evict and picks the cheapest.
# A high value (vs the default 1 for ungrouped models) keeps 120b + vl resident over a
# transient single-model request, so only a genuine state switch — the coder, an image
# render — displaces them.
RESIDENT_EVICT_COST = 100

# Explicit opt-OUT values for LOCAL_LLM_RESIDENT_GROUP: anything else (including
# unset/empty) keeps the recommended set co-resident, since co-residency is the default.
_FALSEY = ("0", "false", "no", "off")


def resolve_weight(root: str, model_id: str, pattern: str) -> str:
    """The weight path (RELATIVE to the model dir) for a model's glob — the first
    shard for a multi-part GGUF (llama.cpp follows it to the rest). Raises if
    nothing matches or a shard set is incomplete, so a partial download fails here,
    not cryptically at gateway load.

    Searches recursively: some repos (Unsloth's UD-Q* quants) nest the shards in a
    quant subdirectory, so `hf download` saves them under `<id>/<quant>/`. The
    return value is relative to `<root>/<id>` (a bare filename at the top level, or
    `<quant>/<file>` when nested) so the gateway's `-m /models/<id>/<rel>` resolves
    either way. hf's `.cache/` download-staging dir is skipped."""
    base = os.path.join(root, model_id)
    matches = sorted(
        m
        for m in glob.glob(os.path.join(base, "**", pattern), recursive=True)
        if ".cache" not in os.path.relpath(m, base).split(os.sep)
    )
    if not matches:
        raise FileNotFoundError(
            f"no file matching {pattern!r} for {model_id} under {root} — download incomplete?"
        )
    rels = [os.path.relpath(m, base) for m in matches]
    shards = [r for r in rels if "-00001-of-" in os.path.basename(r)]
    if shards:
        first = shards[0]
        total = int(os.path.basename(first).split("-of-")[1].split(".gguf")[0])
        if len(matches) != total:
            raise FileNotFoundError(
                f"{model_id}: expected {total} shards for {pattern!r}, found {len(matches)}"
            )
        return first
    return rels[0]


def render(
    models: Sequence[Mapping[str, object]],
    root: str,
    *,
    windows: Mapping[str, int] | None = None,
    resident_group: bool = False,
    pinned: Sequence[str] | None = None,
) -> str:
    """The full llama-swap.yaml text for `models` (catalog manifest dicts). `root`
    is the host path to the weights (globbed to resolve filenames); `windows` maps
    catalog id → context-window override (absent = the model's catalog default).
    `pinned` is the operator's staged set (catalog ids) — those models join the
    co-resident matrix set so staging keeps them loaded alongside the recommended set."""
    windows = windows or {}
    lines = ["# Generated by jbrain.llm.llama_swap_config — do not edit by hand.", "models:"]
    for i, m in enumerate(models):
        port = UPSTREAM_PORT_BASE + i
        model_id = str(m["id"])
        gguf = resolve_weight(root, model_id, str(m["gguf_include"]))
        window = windows.get(model_id, int(cast(int, m["context_window"])))
        cmd = [
            "llama-server",
            "--host",
            "127.0.0.1",
            "--port",
            str(port),
            # The context window. Must match what the router reports to the meter.
            "-c",
            str(window),
            # Tool calling needs the model's own chat template: --jinja makes
            # llama-server render the embedded (Hermes-style for Qwen) tool-use
            # template and parse `<tool_call>` blocks back into structured
            # `tool_calls`. Without it the OpenAI `tools` we send have no grammar
            # behind them and the model free-forms text (a narrated, never-called
            # image) — so the on-box image tools never fire.
            "--jinja",
            # gfx1151 stability/perf flags: flash attention + --no-mmap; -ngl 999
            # offloads every layer to the iGPU.
            "-fa",
            "1",
            "--no-mmap",
            "-m",
            f"/models/{model_id}/{gguf}",
            "-ngl",
            "999",
        ]
        # A thinking model emits its reasoning inline as `<think>…</think>`;
        # `--reasoning-format deepseek` (paired with --jinja above) makes llama.cpp parse
        # those tags out of `content` into the `reasoning_content` channel the shim maps to
        # Anthropic thinking blocks. Empty (the common case) leaves llama.cpp's `auto`.
        reasoning_format = str(m.get("reasoning_format") or "")
        if reasoning_format:
            cmd += ["--reasoning-format", reasoning_format]
        mmproj = m.get("mmproj_include")
        if mmproj:
            cmd += ["--mmproj", f"/models/{model_id}/{resolve_weight(root, model_id, str(mmproj))}"]
        lines.append(f"  {m['served_model']}:")
        lines.append(f"    proxy: http://127.0.0.1:{port}")
        lines.append("    cmd: >")
        lines.append("      " + " ".join(cmd))

    # The co-resident set keeps a chosen group of models loaded together so hot paths
    # skip the cold-load on a task switch — the agent's text model (gpt-oss-120b) and
    # the vision model (qwen3-vl) never swap each other out mid-turn. Two sources union:
    # the install-time `recommended` set (when resident_group is on — now the default,
    # so the box runs 120b + vl co-resident out of the box) and the operator's runtime
    # staged set (`pinned`, catalog ids).
    #
    # Expressed as a llama-swap `matrix` (Groups V2), NOT the legacy `groups`. On a
    # single unified-memory box the three states (120b+vl / coder / image) can't coexist,
    # so a switch MUST evict — the question is only "evict what, and does it come back".
    # `matrix` answers both: `evict_costs` costs the hot set high, so a one-off request
    # for a standalone model (the coder, a batch model) evicts cheaper models first, and
    # the app (jbrain.llm.residency) deliberately re-warms the hot set at end of turn.
    # Legacy `groups` can't express this here: `swap:false` isn't a memory pin (an
    # ungrouped load still evicts it, with no restore), and `persistent:true` would OOM
    # when the coder needs the whole box. A model in no set runs alone — correct for the
    # coder and the big standalone reasoners.
    pinned_ids = set(pinned or ())
    resident = [
        str(m["served_model"])
        for m in models
        if (resident_group and m.get("recommended")) or str(m["id"]) in pinned_ids
    ]
    if resident:
        # Short var aliases (the matrix DSL requires alphanumeric ≤8-char names; served
        # names have hyphens and run long). `mN` by resident-list order — stable across
        # runs since the manifest order is the catalog order.
        aliases = [f"m{i}" for i in range(len(resident))]
        lines.append("matrix:")
        lines.append("  vars:")
        lines += [f"    {alias}: {name}" for alias, name in zip(aliases, resident, strict=True)]
        lines.append("  evict_costs:")
        lines += [f"    {alias}: {RESIDENT_EVICT_COST}" for alias in aliases]
        lines.append("  sets:")
        # One set declaring the whole hot group co-resident (`a & b & …`). A lone member
        # is a bare alias — inert as a combo, but its high evict_cost still makes it sticky.
        lines.append(f'    resident: "{" & ".join(aliases)}"')

    return "\n".join(lines) + "\n"


def write(
    root: str,
    models: Sequence[Mapping[str, object]],
    *,
    windows: Mapping[str, int] | None = None,
    resident_group: bool = False,
    pinned: Sequence[str] | None = None,
) -> str:
    """Render and atomically write {root}/llama-swap.yaml (temp + rename so the
    gateway's --watch-config never sees a half-written file). Returns the path."""
    text = render(models, root, windows=windows, resident_group=resident_group, pinned=pinned)
    path = os.path.join(root, "llama-swap.yaml")
    tmp = f"{path}.tmp"
    with open(tmp, "w") as f:
        f.write(text)
    os.replace(tmp, path)
    return path


def _main(argv: list[str]) -> int:
    """CLI for scripts/local-llm-setup.sh: `... <models_dir>` reads the MANIFEST
    env (catalog JSON) and LOCAL_LLM_RESIDENT_GROUP, writes the config."""
    if len(argv) != 1:
        print("usage: python -m jbrain.llm.llama_swap_config <models_dir>", file=sys.stderr)
        return 2
    root = argv[0]
    models = json.loads(os.environ["MANIFEST"])
    # Co-residency is the DEFAULT (the box runs the recommended set — 120b + vl — hot
    # together): unset/empty reads as on. An operator on a memory-tight box opts OUT with
    # an explicit false-y value (`LOCAL_LLM_RESIDENT_GROUP=0`).
    resident = os.environ.get("LOCAL_LLM_RESIDENT_GROUP", "").strip().lower() not in _FALSEY
    path = write(root, models, resident_group=resident)
    kept = len([m for m in models if m.get("recommended")]) if resident else 0
    print(f"wrote {path}: {len(models)} model(s); {kept} kept co-resident (others swap alone)")
    return 0


if __name__ == "__main__":
    raise SystemExit(_main(sys.argv[1:]))
